// forceUTF8 checks if the provided byte slice is valid UTF-8.
// If it is, it returns the slice untouched.
// If not, it assumes the text is encoded in Windows-1252 (the most common
// encoding for legacy license files containing smart quotes) and decodes it
// into valid UTF-8.
func forceUTF8(data []byte) []byte {
	if utf8.Valid(data) {
		return data
	}

	var buf bytes.Buffer
	// Windows-1252 is a single-byte encoding
	for _, b := range data {
		if b < 0x80 {
			buf.WriteByte(b)
		} else {
			buf.WriteRune(windows1252[b-0x80])
		}
	}
	return buf.Bytes()
}
