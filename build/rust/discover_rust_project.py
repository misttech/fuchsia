"""Discovers and merges rust-project.json files from GN and Bazel builds.

This script is intended to be used as a `rust-analyzer.discoverConfig` script in
VSCode. It reads the `rust-project.json` generated by GN, and merges it with
`rust-project.json` files generated by Bazel for specific targets. This script
does NOT change any files on disk, it prints the merged rust-project.json to
stdout to be consumed by rust-analyzer.
"""

import argparse
import datetime
import json
import os
import sys
from pathlib import Path

_SCRIPT_DIR = Path(__file__).parent
_BUILD_BAZEL_SCRIPTS = _SCRIPT_DIR / ".." / ".." / "build" / "bazel" / "scripts"
sys.path.insert(0, str(_BUILD_BAZEL_SCRIPTS))
import bazel_rust_analyzer_utils

# Set this to True to debug operations locally in this script.
#
# If True:
#   - Debug information will be written to _DEBUG_FILE.
#   - The last merged rust-project.json will be written to _DEBUG_RUST_PROJECT_JSON.
_DEBUG = False
_DEBUG_FILE = "/tmp/fuchsia_discover_rust_project.log"
_DEBUG_RUST_PROJECT_JSON = "/tmp/fuchsia_discover_rust_project_last.json"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--workspace-dir", type=Path, required=True)
    parser.add_argument("--discover-args", type=str, required=True)
    args = parser.parse_args()

    discoverArgs = json.loads(args.discover_args)
    source = Path(discoverArgs["path"])
    if _DEBUG:
        with open(_DEBUG_FILE, "a") as f:
            timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            f.write(f"[{timestamp}] discover_args: {discoverArgs}\n")

    gn_rust_project_json = args.workspace_dir / "rust-project.json"
    with open(gn_rust_project_json, "r") as f:
        gn_rust_project = json.load(f)

    build_dir_file = args.workspace_dir / ".fx-build-dir"
    build_dir = Path(build_dir_file.read_text().strip())

    with open(build_dir / "bazel_build_action_targets.json", "r") as f:
        bazel_build_action_targets = json.load(f)

    jsons_to_merge = []
    for target in bazel_build_action_targets:
        if "bazel_rust_project_json" not in target:
            continue

        p = build_dir / target["bazel_rust_project_json"]
        if not p.exists():
            if _DEBUG:
                with open(_DEBUG_FILE, "a") as f:
                    timestamp = datetime.datetime.now().strftime(
                        "%Y-%m-%d %H:%M:%S"
                    )
                    f.write(f"[{timestamp}] File {p} does not exist.\n")

            # Skip this target, it's possible that this target is not built yet.
            #
            # This means the merged rust-project.json will be incomplete, but it's better than
            # erroring out here and not having any rust-project.json at all.
            continue

        with open(p, "r") as f:
            jsons_to_merge.append(json.load(f))

    merged_rust_project_json = (
        bazel_rust_analyzer_utils.merge_rust_project_jsons(
            gn_rust_project, jsons_to_merge
        )
    )

    if os.environ.get("FX_RA_EXPERIMENTS_ENABLED"):
        crate_for_file = bazel_rust_analyzer_utils.find_crate_for_file(
            source, merged_rust_project_json["crates"]
        )
        if crate_for_file:
            merged_rust_project_json[
                "crates"
            ] = bazel_rust_analyzer_utils.get_crate_and_dependencies(
                crate_for_file, merged_rust_project_json["crates"]
            )
        else:
            # No matching crate found, return everything.
            if _DEBUG:
                with open(_DEBUG_FILE, "a") as f:
                    timestamp = datetime.datetime.now().strftime(
                        "%Y-%m-%d %H:%M:%S"
                    )
                    f.write(
                        f"[{timestamp}] File {source} does not belong to any crate from merged rust-project.json.\n"
                    )

    if _DEBUG:
        with open(_DEBUG_RUST_PROJECT_JSON, "w") as f:
            json.dump(merged_rust_project_json, f, indent=2)

    discover_config_result = {
        "kind": "finished",
        "buildfile": "/dev/null",
        "project": merged_rust_project_json,
    }

    print(json.dumps(discover_config_result))

    return 0


if __name__ == "__main__":
    sys.exit(main())
