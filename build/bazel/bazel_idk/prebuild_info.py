"""Generates prebuild info that includes Bazel IDK atoms."""

import argparse
import json
import sys
import typing as T
from pathlib import Path

# Assume the script is in //build/bazel/bazel_idk/.
_CURRENT_DIR = Path(__file__).parent
_SCRIPT_DIR = _CURRENT_DIR.parent / "scripts"
sys.path.insert(0, str(_SCRIPT_DIR))

import build_utils

# Location of the file containing the Starlark cquery code to run in this script
# to locate all test targets.
_CQUERY_STARLARK_FILE_PATH = _CURRENT_DIR / "idk_prebuild_info.cquery"


class PrebuildInfoGenerator:
    def __init__(
        self,
        fuchsia_dir: Path | None,
        build_dir: Path | None,
    ):
        """Initializes the generator.

        Args:
            fuchsia_dir: Path to the Fuchsia source directory.
            build_dir: Path to the Fuchsia build directory.
        """
        self.bazel_cmd = build_utils.BazelCommand.from_dirs(
            fuchsia_dir=fuchsia_dir,
            build_dir=build_dir,
        )

    def generate_prebuild_info_for_target(self, target: str) -> T.Any:
        """Generates prebuild info for the specified Bazel target.

        Args:
            target: The Bazel target for which to generate prebuild info.
        Returns:
            A list of prebuild info dictionaries.
        """
        output = self.bazel_cmd.run(
            "cquery",
            [
                "--output=starlark",
                f"--starlark:file={_CQUERY_STARLARK_FILE_PATH.resolve()}",
                target,
            ],
        )
        assert output, "Bazel command failed."
        return json.loads(output)

    def merge_bazel_prebuild_info(
        self,
        idk_molecule_target: str,
        gn_prebuild_info_path: Path,
        merged_prebuild_info_path: Path,
    ) -> None:
        """Merges the prebuild info from Bazel into the file generated by GN.

        Note: There is no de-duplication. Each atom must only be defined in
        either Bazel or GN.

        Note: The formatting of empty arrays is different from the original
        GN-generated file.

        Args:
            idk_molecule_target: The Bazel IDK molecule representing the IDK collection.
            gn_prebuild_info_path: Path to the prebuild info JSON file generated by GN.
            merged_prebuild_info_path: Path to write the merged prebuild info JSON file.
        """
        bazel_prebuild_info = self.generate_prebuild_info_for_target(
            idk_molecule_target
        )

        with gn_prebuild_info_path.open("r") as f:
            gn_prebuild_info = json.load(f)

        gn_prebuild_info.extend(bazel_prebuild_info)

        with merged_prebuild_info_path.open("w") as f:
            json.dump(gn_prebuild_info, f, indent=2)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--fuchsia-dir",
        type=Path,
        help="Path to the Fuchsia source directory.",
    )
    parser.add_argument(
        "--build-dir",
        type=Path,
        help="Path to the Fuchsia build directory.",
    )
    parser.add_argument(
        "--idk-molecule-target",
        type=str,
        required=True,
        help="The Bazel IDK molecule representing the IDK collection.",
    )
    parser.add_argument(
        "--gn-prebuild-info-path",
        type=Path,
        required=True,
        help="Path to the prebuild info JSON file generated by GN for the same IDK collection as `--idk-molecule-target`.",
    )
    parser.add_argument(
        "--merged-prebuild-info-path",
        type=Path,
        required=True,
        help="Path to write the merged prebuild info JSON file.",
    )

    args = parser.parse_args()

    generator = PrebuildInfoGenerator(args.fuchsia_dir, args.build_dir)
    generator.merge_bazel_prebuild_info(
        args.idk_molecule_target,
        args.gn_prebuild_info_path,
        args.merged_prebuild_info_path,
    )

    return 0


if __name__ == "__main__":
    sys.exit(main())
