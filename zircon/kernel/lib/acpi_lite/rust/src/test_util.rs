pub struct NullPhysMemReader;

impl PhysMemReader for NullPhysMemReader {
    fn phys_to_slice(&self, _phys: usize, _length: usize) -> Result<&[u8], Status> {
        Err(Status::OUT_OF_RANGE)
    }
}

pub struct EmptyPhysMemReader {
    empty_data: std::vec::Vec<u8>,
}

impl EmptyPhysMemReader {
    pub fn new() -> Self {
        Self { empty_data: std::vec![0; 16 * 1024] }
    }
}

impl PhysMemReader for EmptyPhysMemReader {
    fn phys_to_slice(&self, _phys: usize, length: usize) -> Result<&[u8], Status> {
        if length >= 16 * 1024 {
            return Err(Status::OUT_OF_RANGE);
        }
        Ok(&self.empty_data[..length])
    }
}

#[derive(Clone)]
pub struct FakeRegion<'a> {
    pub phys_addr: usize,
    pub data: &'a [u8],
}

pub struct FakePhysMemReader<'a> {
    rsdp: usize,
    regions: std::vec::Vec<FakeRegion<'a>>,
}

impl<'a> FakePhysMemReader<'a> {
    pub fn new(rsdp: usize, regions: &[FakeRegion<'a>]) -> Self {
        Self { rsdp, regions: regions.to_vec() }
    }

    pub fn rsdp(&self) -> usize {
        self.rsdp
    }

    pub fn regions(&self) -> &[FakeRegion<'a>] {
        &self.regions
    }
}

impl<'a> PhysMemReader for FakePhysMemReader<'a> {
    fn phys_to_slice(&self, phys: usize, length: usize) -> Result<&[u8], Status> {
        for region in &self.regions {
            if region.phys_addr == phys && length <= region.data.len() {
                return Ok(&region.data[..length]);
            }
        }
        Err(Status::NOT_FOUND)
    }
}

pub struct FakeAcpiParser<'a> {
    tables: std::vec::Vec<&'a AcpiSdtHeader>,
}

impl<'a> FakeAcpiParser<'a> {
    pub fn new(tables: &[&'a [u8]]) -> Self {
        let mut parsed_tables = std::vec::Vec::new();
        for table in tables {
            assert!(table.len() >= core::mem::size_of::<AcpiSdtHeader>());
            // SAFETY: We asserted that `table` has at least `size_of::<AcpiSdtHeader>()` bytes.
            let r = zerocopy::Ref::<_, AcpiSdtHeader>::from_bytes(
                &table[..core::mem::size_of::<AcpiSdtHeader>()],
            )
            .unwrap();
            let header = zerocopy::Ref::into_ref(r);
            parsed_tables.push(header);
        }
        Self { tables: parsed_tables }
    }

    pub fn new_from_headers(tables: &[&'a AcpiSdtHeader]) -> Self {
        Self { tables: tables.to_vec() }
    }
}

impl<'a> AcpiParserInterface for FakeAcpiParser<'a> {
    fn num_tables(&self) -> usize {
        self.tables.len()
    }

    fn get_table_at_index(&self, index: usize) -> Option<&AcpiSdtHeader> {
        self.tables.get(index).copied()
    }
}

